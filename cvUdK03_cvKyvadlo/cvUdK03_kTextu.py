def fcn_rovniceKyvadla(x, t, u):

    x_1, x_2 = x

    dotx_1 = x_2
    dotx_2 = -(beta/m*l**2) * x_2 - (g/l) * np.sin(x_1) + (1.0/m*l**2) * u

    return [dotx_1, dotx_2]

